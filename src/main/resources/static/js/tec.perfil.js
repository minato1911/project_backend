var SERVICE_LIMIT=30*60;
function toSec(s){ if(!s) return 0; var p=s.split(':'); return (+p[0])*3600+(+p[1])*60+(+(p[2]||0)); }
function fmtDur(sec){ var h=Math.floor(sec/3600),m=Math.floor((sec%3600)/60); return (h>0?h+'h ':'')+m+'min'; }

function loadMyServices(){
  var op=JSON.parse(localStorage.getItem('bat-op-tickets')||'[]');
  var tech=JSON.parse(localStorage.getItem('bat-tech-tickets')||'[]');
  var all=op.slice();
  tech.forEach(function(tt){ var i=all.findIndex(function(x){return x.id===tt.id;}); if(i>=0) all[i]=Object.assign({},all[i],tt); else all.push(tt); });
  var mine=all.filter(function(t){ return (String(t.status)==='4'||t.finalStatus||t.dateEnd) && t.tech===TECH_NAME; });
  if(!mine.length){
    // fallback sample numbers consistent with the história page
    mine=[
      {serviceTime:'00:25:00',travelTime:'00:15:00'},
      {serviceTime:'00:48:00',travelTime:'00:12:00'},
      {serviceTime:'00:35:00',travelTime:'00:09:00'},
      {serviceTime:'02:10:00',travelTime:'00:20:00'}
    ];
  }
  return mine;
}
function renderKPIs(){
  var mine=loadMyServices();
  var total=mine.length;
  var svcSecs=mine.map(function(t){return toSec(t.serviceTime);}).filter(function(s){return s>0;});
  var trvSecs=mine.map(function(t){return toSec(t.travelTime);}).filter(function(s){return s>0;});
  var avgSvc=svcSecs.length?Math.round(svcSecs.reduce(function(a,b){return a+b;},0)/svcSecs.length):0;
  var avgTrv=trvSecs.length?Math.round(trvSecs.reduce(function(a,b){return a+b;},0)/trvSecs.length):0;
  var onTime=mine.filter(function(t){return toSec(t.serviceTime)<=SERVICE_LIMIT;}).length;
  var eff=total?Math.round((onTime/total)*100):0;
  animV(document.getElementById('kpi-done'),total,'');
  document.getElementById('kpi-svc').textContent=fmtDur(avgSvc);
  document.getElementById('kpi-trv').textContent=fmtDur(avgTrv);
  // efficiency ring
  var r=36,cx=42,cy=42,circ=2*Math.PI*r,len=(eff/100)*circ;
  var color=eff>=80?'#2D9E6B':eff>=50?'#005691':'#E8A020';
  document.getElementById('eff-svg').innerHTML=
    '<circle cx="42" cy="42" r="36" fill="none" stroke="var(--br)" stroke-width="9"/>'+
    '<circle cx="42" cy="42" r="36" fill="none" stroke="'+color+'" stroke-width="9" stroke-linecap="round" stroke-dasharray="'+len+' '+(circ-len)+'"/>';
  document.getElementById('eff-pct').textContent=eff+'%';
  var st=document.getElementById('eff-status');
  if(eff>=80){ st.textContent=T('eff_exc'); st.className='eff-status exc'; }
  else if(eff>=50){ st.textContent=T('eff_good'); st.className='eff-status good'; }
  else { st.textContent=T('eff_reg'); st.className='eff-status reg'; }
}
function animV(el,tgt,suffix){ if(!el)return; var from=parseInt(el.textContent)||0,dur=700,st=performance.now(); function step(now){var p=Math.min((now-st)/dur,1),e=1-Math.pow(1-p,3);el.textContent=Math.round(from+(tgt-from)*e)+(suffix||'');if(p<1)requestAnimationFrame(step);} requestAnimationFrame(step); }

