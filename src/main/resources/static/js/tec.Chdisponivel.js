  var btn=document.getElementById('m-assume-btn');
  var s=uiStatus(t);
  var curId=getCurrentTicketId();
  if(t.id===curId){ btn.disabled=true; btn.innerHTML='<i class="fa-solid fa-user-check"></i> '+T('btn_assumed'); }
  else if(s==='available' && !hasCurrent()){ btn.disabled=false; btn.innerHTML='<i class="fa-solid fa-hand-pointer"></i> '+T('m_assume'); }
  else { btn.disabled=true; btn.innerHTML='<i class="fa-solid fa-lock"></i> '+(s==='available'?T('m_assume'):T('btn_taken')); }
