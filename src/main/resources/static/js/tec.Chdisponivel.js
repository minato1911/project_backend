function render(){
  var fi=getFiltered(),sorted=sortData(fi),total=sorted.length;
  var tp=Math.max(1,Math.ceil(total/PER)); if(page>tp) page=1;
  var st=(page-1)*PER, pg=sorted.slice(st,st+PER);
  var locked=hasCurrent();
  var curId=getCurrentTicketId();
  var tbody=document.getElementById('tbody');
  if(!total){
    tbody.innerHTML='<tr><td colspan="9"><div class="empty"><i class="fa-solid fa-inbox"></i><strong>'+T('no_results')+'</strong><p>'+T('no_results_s')+'</p></div></td></tr>';
  } else {
    tbody.innerHTML=pg.map(function(t){
      var s=uiStatus(t);
      var isMine=(t.id===curId);
      var actionBtn;
      if(isMine){
        actionBtn='<span class="mine-tag"><i class="fa-solid fa-user-check"></i> '+T('btn_assumed')+'</span>';
      } else if(s==='available' && !locked){
        actionBtn='<button class="btn assume xs" onclick="assumeTicket(\''+t.id+'\')"><i class="fa-solid fa-hand-pointer"></i> '+T('btn_assume')+'</button>';
      } else if(s==='available' && locked){
        actionBtn='<button class="btn assume xs" disabled title="'+T('toast_already')+'"><i class="fa-solid fa-lock"></i> '+T('btn_assume')+'</button>';
      } else {
        actionBtn='<button class="btn xs" disabled><i class="fa-solid fa-ban"></i> '+T('btn_taken')+'</button>';
      }
      var cells=[
        '<span style="font-family:monospace;font-size:12.5px;font-weight:700;color:var(--bm)">'+t.id+'</span>',
        '<span style="font-size:12.5px">'+t.operator+'</span>',
        '<span style="font-size:12px">'+t.sector+'</span>',
        '<span style="font-size:12.5px;font-weight:600">'+t.equip+'</span>',
        '<span class="prob-cell" title="'+t.problem.replace(/"/g,"&quot;")+'">'+t.problem+'</span>',
        prioBadge(t.priority),
        '<span style="font-size:11.5px;color:var(--mu)">'+timeAgo(t.date)+'</span>',
        statusBadge(t),
        '<div class="act-btns"><button class="btn ghost xs" onclick="openModal(\''+t.id+'\')"><i class="fa-solid fa-eye"></i></button>'+actionBtn+'</div>'
      ];
      return '<tr class="'+(isMine?'mine':'')+'">'+cells.map(function(c){return '<td>'+c+'</td>';}).join('')+'</tr>';
    }).join('');
  }
  var psi=document.getElementById('panel-sub');
  if(psi) psi.textContent=T('showing')+' '+(total?st+1:0)+'-'+Math.min(st+PER,total)+' '+T('of')+' '+total+' '+T('records');
  var pi=document.getElementById('pg-inf');
  if(pi) pi.innerHTML=T('showing')+' <strong>'+Math.min(st+1,total)+'-'+Math.min(st+PER,total)+'</strong> '+T('of')+' <strong>'+total+'</strong>';
  var pb=document.getElementById('pg-btns');
  if(pb){
    var h='<button class="pp" onclick="goPage('+(page-1)+')" '+(page<=1?'disabled':'')+'><i class="fa-solid fa-chevron-left"></i></button>';
    for(var i=1;i<=tp;i++){ if(i===1||i===tp||Math.abs(i-page)<=1) h+='<button class="pp '+(i===page?'act':'')+'" onclick="goPage('+i+')">'+i+'</button>'; else if(Math.abs(i-page)===2) h+='<span class="pp" style="cursor:default">...</span>'; }
    h+='<button class="pp" onclick="goPage('+(page+1)+')" '+(page>=tp?'disabled':'')+'><i class="fa-solid fa-chevron-right"></i></button>';
    pb.innerHTML=h;
  }
}
