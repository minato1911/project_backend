/* ============ FILTER & SORT ============ */
function getFiltered(){
  var q=(document.getElementById('srch').value||'').toLowerCase();
  var fp=document.getElementById('f-prio').value;
  var fs=document.getElementById('f-sec').value;
  var fst=document.getElementById('f-status').value;
  var d1=document.getElementById('f-d1').value;
  var d2=document.getElementById('f-d2').value;
  return tickets.filter(function(t){
    if(q && !(t.id+t.operator+t.equip+t.problem+t.sector).toLowerCase().includes(q)) return false;
    if(fp && t.priority!==fp) return false;
    if(fs && t.sector!==fs) return false;
    if(fst){
      var s=uiStatus(t);
      if(fst==='0' && s!=='available') return false;
      if(fst==='at' && s!=='inservice') return false;
      if(fst==='4' && s!=='finished') return false;
    }
    if(d1 && parseDate(t.date)<new Date(d1)) return false;
    if(d2 && parseDate(t.date)>new Date(d2+'T23:59:59')) return false;
    return true;
  });
}
function sortData(arr){
  var col=sortCol,dir=sortDir,po={alta:3,media:2,baixa:1};
  return arr.slice().sort(function(a,b){
    if(col==='prio'){ var d=(po[b.priority]||0)-(po[a.priority]||0); return dir==='asc'?-d:d; }
    if(col==='id'){ var d=a.id.localeCompare(b.id); return dir==='asc'?d:-d; }
    if(col==='operator'){ var d=a.operator.localeCompare(b.operator); return dir==='asc'?d:-d; }
    if(col==='sector'){ var d=a.sector.localeCompare(b.sector); return dir==='asc'?d:-d; }
    var da=parseDate(a.date),db=parseDate(b.date); return dir==='asc'?da-db:db-da;
  });
}
