/* ═══ STATS ═══ */
function updateStats(){
  const on=machines.filter(m=>m.status==='online').length;
  const off=machines.filter(m=>m.status==='offline').length;
  const ma=machines.filter(m=>m.status==='manutencao').length;
  animateVal('cnt-online',on);animateVal('cnt-offline',off);animateVal('cnt-maint',ma);
  document.getElementById('sb-count').textContent=machines.length;
}
function animateVal(id,target){
  const el=document.getElementById(id);if(!el)return;
  const start=performance.now();const from=parseInt(el.textContent)||0;const dur=700;
  function step(now){const p=Math.min((now-start)/dur,1);const e=1-Math.pow(1-p,3);el.textContent=Math.round(from+(target-from)*e);if(p<1)requestAnimationFrame(step);}
  requestAnimationFrame(step);
}

/* ═══ STATUS BADGE HTML ═══ */
function statusBadge(status){
  const d=TR[currentLang]||TR['pt-BR'];
  const map={online:{cls:'online',label:d.status_online},manutencao:{cls:'manutencao',label:d.status_maint},offline:{cls:'offline',label:d.status_offline}};
  const s=map[status]||{cls:status,label:status};
  return `<span class="status-badge ${s.cls}"><span class="status-dot"></span>${s.label}</span>`;
}

/* ═══ TABLE ═══ */
function getFiltered(){
  const q=document.getElementById('search-input').value.toLowerCase();
  const st=document.getElementById('filter-status').value;
  const se=document.getElementById('filter-sector').value;
  return machines.filter(m=>{
    const matchQ=!q||m.name.toLowerCase().includes(q)||m.sector.toLowerCase().includes(q)||m.id.toLowerCase().includes(q)||m.model.toLowerCase().includes(q);
    const matchSt=!st||m.status===st;
    const matchSe=!se||m.sector===se;
    return matchQ&&matchSt&&matchSe;
  });
}

function renderHighlights(){
  const board=document.getElementById('highlights-grid');
  if(!board) return;
  const d=TR[currentLang]||TR['pt-BR'];
  const online=machines.filter(m=>m.status==='online').length;
  const maint=machines.filter(m=>m.status==='manutencao').length;
  const offline=machines.filter(m=>m.status==='offline').length;
  board.innerHTML=`
    <div class="highlight-card"><strong>${online} em operação</strong><span>${d.status_online} · ${d.stat_active_sub}</span></div>
    <div class="highlight-card"><strong>${maint} em manutenção</strong><span>${d.status_maint} · ${d.stat_maint_sub}</span></div>
    <div class="highlight-card"><strong>${offline} fora de linha</strong><span>${d.status_offline} · ${d.stat_offline_sub}</span></div>`;
}

function renderTable(){
  const d=TR[currentLang]||TR['pt-BR'];
  const filtered=getFiltered();
  const total=filtered.length;
  const totalPages=Math.max(1,Math.ceil(total/PER_PAGE));
  if(currentPage>totalPages)currentPage=1;
  const start=(currentPage-1)*PER_PAGE;
  const page=filtered.slice(start,start+PER_PAGE);
  const tbody=document.getElementById('table-body');
  tbody.innerHTML=page.map((m,i)=>{
    const ri=machines.indexOf(m);
    return `<tr>
      <td class="td-id">${m.id}</td>
      <td><div class="machine-name">${m.name}</div></td>
      <td><span class="machine-sector">${m.model}</span></td>
      <td>${m.sector}</td>
      <td>${statusBadge(m.status)}</td>
      <td>${m.last_maint}</td>
      <td><div class="action-btns">
        <button class="act-btn edit" onclick="openEditModal(${ri})" title="${d.tip_edit}"><i class="fa-solid fa-pen"></i></button>
        <button class="act-btn view" onclick="viewMachine(${ri})" title="${d.tip_view}"><i class="fa-solid fa-eye"></i></button>
        <button class="act-btn del" onclick="deleteMachine(${ri})" title="${d.tip_del}"><i class="fa-solid fa-trash"></i></button>
      </div></td>
    </tr>`;
  }).join('');
  document.getElementById('table-count-info').textContent=`${d.showing} ${Math.min(start+1,total)}-${Math.min(start+PER_PAGE,total)} ${d.of} ${total} ${d.records}`;
  document.getElementById('pag-info').innerHTML=`${d.showing} <strong>${Math.min(start+1,total)}-${Math.min(start+PER_PAGE,total)}</strong> ${d.of} <strong>${total}</strong> ${d.records}`;
  renderPagination(totalPages,d);
}

function renderPagination(total,d){
  const btns=document.getElementById('pag-btns');
  let html=`<button class="pag-btn" onclick="goPage(${currentPage-1})" ${currentPage<=1?'disabled':''}><i class="fa-solid fa-chevron-left"></i></button>`;
  for(let i=1;i<=total;i++){if(i===1||i===total||Math.abs(i-currentPage)<=1){html+=`<button class="pag-btn ${i===currentPage?'active':''}" onclick="goPage(${i})">${i}</button>`;}else if(Math.abs(i-currentPage)===2){html+=`<button class="pag-btn" disabled>…</button>`;}}
  html+=`<button class="pag-btn" onclick="goPage(${currentPage+1})" ${currentPage>=total?'disabled':''}><i class="fa-solid fa-chevron-right"></i></button>`;
  btns.innerHTML=html;
}

function goPage(p){const filtered=getFiltered();const total=Math.max(1,Math.ceil(filtered.length/PER_PAGE));if(p<1||p>total)return;currentPage=p;renderTable();}

