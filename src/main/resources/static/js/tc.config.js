function applyTheme(mode){
  var actual=mode;
  if(mode==='auto'){ actual=window.matchMedia&&window.matchMedia('(prefers-color-scheme: dark)').matches?'dark':'light'; }
  document.documentElement.setAttribute('data-theme',actual);
  document.getElementById('theme-ico').className=actual==='dark'?'fa-solid fa-sun':'fa-solid fa-moon';
}
