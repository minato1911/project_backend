function init(){
  var lang=localStorage.getItem('bat-lang')||'pt'; CL=lang;
  document.getElementById('lang-lbl').textContent=lang.toUpperCase();
  document.querySelectorAll('.lo').forEach(function(el,i){ el.classList.toggle('active',['pt','en','es','de','ru'][i]===lang); });
  initTheme();
  var hc=localStorage.getItem('bat-ct-hc')||localStorage.getItem('bat-op-hc')||'0';
  if(hc==='1'){ document.documentElement.setAttribute('data-hc','1'); document.getElementById('sw-hc').checked=true; }
  var libras=localStorage.getItem('bat-ct-libras')||'1';
  if(libras==='0'){ document.getElementById('sw-libras').checked=false; document.querySelector('.libras-wrap').style.display='none'; }
  var font=localStorage.getItem('bat-ct-font')||'1';
  document.documentElement.style.fontSize=(font*100)+'%'; document.getElementById('font-range').value=font; document.getElementById('font-val').textContent=Math.round(font*100)+'%';
  var rm=localStorage.getItem('bat-ct-rm')||'0'; if(rm==='1'){ document.getElementById('sw-rm').checked=true; toggleRM(document.getElementById('sw-rm')); }
  if((localStorage.getItem('bat-ct-simple')||'0')==='1') document.getElementById('sw-simple').checked=true;
  if((localStorage.getItem('bat-ct-reader')||'0')==='1') document.getElementById('sw-reader').checked=true;
  // notifications
  [['sw-new','notif-new','1'],['sw-urgent','notif-urgent','1'],['sw-exceeded','notif-exceeded','1'],['sw-sound','notif-sound','0'],['sw-auto-alert','auto-alert','1'],['sw-pause-allow','pause-allow','1']].forEach(function(t){
    var v=localStorage.getItem('bat-ct-'+t[1]); var on=(v!==null)?(v==='1'):(t[2]==='1'); document.getElementById(t[0]).checked=on;
  });
  // atendimento
  document.getElementById('svc-time').value=localStorage.getItem('bat-ct-svc-time')||30;
  document.getElementById('pause-limit').value=localStorage.getItem('bat-ct-pause-limit')||10;
  applyTR();
  initUser();
  syncThemeCards(); syncLangCards();
  buildSecurity(); renderSessions(); renderLogins(); renderDevices();
}
document.addEventListener('DOMContentLoaded',init);