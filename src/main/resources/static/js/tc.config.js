var sessions=[], logins=[], devices=[];
function f(d){ var dd=String(d.getDate()).padStart(2,'0'),mm=String(d.getMonth()+1).padStart(2,'0'),hh=String(d.getHours()).padStart(2,'0'),mi=String(d.getMinutes()).padStart(2,'0'); return dd+'/'+mm+'/'+d.getFullYear()+' '+hh+':'+mi; }
function buildSecurity(){
  var now=new Date();
  function ago(m){ return f(new Date(now-m*60000)); }
  sessions=[{id:'s1',device:'win',ip:'192.168.0.62',time:ago(3),current:true},{id:'s2',device:'android',ip:'10.0.3.21',time:ago(720),current:false}];
  logins=[{device:'win',ip:'192.168.0.62',time:ago(3)},{device:'android',ip:'10.0.3.21',time:ago(720)},{device:'win',ip:'192.168.0.62',time:ago(1500)},{device:'ios',ip:'10.0.3.44',time:ago(3000)}];
  devices=[{device:'win',name:'dev_win',last:ago(3)},{device:'android',name:'dev_android',last:ago(720)},{device:'ios',name:'dev_ios',last:ago(3000)}];
}
var devIcons={win:'fa-laptop',android:'fa-mobile-screen',mac:'fa-laptop',ios:'fa-mobile-screen-button'};
var devKeys={win:'dev_win',android:'dev_android',mac:'dev_mac',ios:'dev_ios'};
function renderSessions(){
  document.getElementById('session-list').innerHTML=sessions.map(function(s){
    var icoCls=s.current?'session-ico current':'session-ico';
    var right=s.current?'<span class="session-badge">'+T('acc_current')+'</span>':'<button class="session-revoke" onclick="revokeSession(\''+s.id+'\')">'+T('revoke')+'</button>';
    return '<div class="session-item"><div class="'+icoCls+'"><i class="fa-solid '+(devIcons[s.device]||'fa-laptop')+'"></i></div><div class="session-info"><strong>'+T(devKeys[s.device])+'</strong><span>'+s.ip+' · '+s.time+'</span></div>'+right+'</div>';
  }).join('');
}
function revokeSession(id){ sessions=sessions.filter(function(s){return s.id!==id;}); renderSessions(); showToast(T('toast_revoked'),'suc'); }
function renderLogins(){
  document.getElementById('login-list').innerHTML=logins.map(function(a){
    return '<div class="session-item"><div class="session-ico"><i class="fa-solid '+(devIcons[a.device]||'fa-laptop')+'"></i></div><div class="session-info"><strong>'+T(devKeys[a.device])+'</strong><span>'+a.ip+' · '+a.time+'</span></div></div>';
  }).join('');
}
function renderDevices(){
  document.getElementById('devices-list').innerHTML=devices.map(function(d){
    return '<div class="session-item"><div class="session-ico"><i class="fa-solid '+(devIcons[d.device]||'fa-laptop')+'"></i></div><div class="session-info"><strong>'+T(d.name)+'</strong><span>'+d.last+'</span></div><span class="session-badge">'+T('authorized')+'</span></div>';
  }).join('');
}

