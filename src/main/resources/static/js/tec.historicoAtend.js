/* ============ FILTER & SORT ============ */
function getFiltered(){
  var q=(document.getElementById('srch').value||'').toLowerCase();
  var ft=document.getElementById('f-tech').value;
  var fs=document.getElementById('f-sec').value;
  var fp=document.getElementById('f-prio').value;
  var d1=document.getElementById('f-d1').value;
  var d2=document.getElementById('f-d2').value;
  return atendimentos.filter(function(t){
    if(q && !(t.id+t.operator+t.equip+t.problem+t.sector+t.tech).toLowerCase().includes(q)) return false;
    if(ft && t.tech!==ft) return false;
    if(fs && t.sector!==fs) return false;
    if(fp && t.priority!==fp) return false;
    if(d1 && parseDate(t.dateEnd)<new Date(d1)) return false;
    if(d2 && parseDate(t.dateEnd)>new Date(d2+'T23:59:59')) return false;
    return true;
  });
}
function sortData(arr){
  var sv=document.getElementById('f-sort').value, col=sortCol, dir=sortDir, po={alta:3,media:2,baixa:1};
  return arr.slice().sort(function(a,b){
    if(sv==='svc-desc'||col==='svc'){ var d=b.svcSec-a.svcSec; return (col==='svc'&&dir==='asc')?-d:d; }
    if(sv==='prio'||col==='prio'){ var d=(po[b.priority]||0)-(po[a.priority]||0); return d!==0?d:parseDate(b.dateEnd)-parseDate(a.dateEnd); }
    if(col==='id'){ var d=a.id.localeCompare(b.id); return dir==='asc'?d:-d; }
    if(col==='operator'){ var d=a.operator.localeCompare(b.operator); return dir==='asc'?d:-d; }
    if(col==='sector'){ var d=a.sector.localeCompare(b.sector); return dir==='asc'?d:-d; }
    if(col==='open'){ var d=parseDate(a.dateOpen)-parseDate(b.dateOpen); return dir==='asc'?d:-d; }
    var da=parseDate(a.dateEnd),db=parseDate(b.dateEnd); return (sv==='end-asc'||dir==='asc')?da-db:db-da;
  });
}
