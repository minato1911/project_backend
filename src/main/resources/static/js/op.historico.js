function exportExcel(){
  var data=sortData(getFiltered());
  if(!data.length){ showToast(T('toast_no_data'),'err'); return; }
  var headers=[T('th_id'),T('th_open'),T('th_end'),T('th_sec'),T('th_equip'),T('th_prob'),T('th_tech'),T('th_time'),T('th_status')];
  var rows=data.map(function(t){
    return [t.id,t.dateOpen,t.dateEnd,t.sector,t.equip,t.problem,t.tech,sumTimes(t.travelTime,t.serviceTime),(t.finalStatus==='cancelled'?T('status_cancelled'):T('status_done'))];
  });
  // Build CSV (Excel-compatible with BOM + semicolon separator)
  function esc(v){ v=String(v).replace(/"/g,'""'); return '"'+v+'"'; }
  var csv=headers.map(esc).join(';')+'\n'+rows.map(function(r){return r.map(esc).join(';');}).join('\n');
  var bom='\uFEFF';
  var blob=new Blob([bom+csv],{type:'text/csv;charset=utf-8;'});
  var url=URL.createObjectURL(blob);
  var a=document.createElement('a');
  var now=new Date();
  var fname='historico_chamados_'+now.getFullYear()+String(now.getMonth()+1).padStart(2,'0')+String(now.getDate()).padStart(2,'0')+'.csv';
  a.href=url; a.download=fname; document.body.appendChild(a); a.click();
  document.body.removeChild(a); URL.revokeObjectURL(url);
  showToast(T('toast_excel'),'suc');
}

