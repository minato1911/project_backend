/* TABLE */
function renderTable(){
  const d   = TR[currentLang] || TR['pt-BR'];
  const fil = getFiltered();
  const tot = fil.length;
  const totalPages = Math.max(1, Math.ceil(tot / PER_PAGE));
  if(currentPage > totalPages) currentPage = 1;
  const start = (currentPage-1) * PER_PAGE;
  const page  = fil.slice(start, start+PER_PAGE);
  const tbody = document.getElementById('table-body');
  tbody.innerHTML = page.map(u => {
    const ri = users.indexOf(u);
    return `<tr>
      <td>
        <div class="tbl-user-col">
          <div style="position:relative">
            ${avatarHTML(u.name, 36, 'flex-shrink:0')}
            <span class="tbl-online ${u.online?'on':'off'}"></span>
          </div>
          <div class="tbl-user-info">
            <strong>${u.name}</strong>
            <span>${u.email}</span>
          </div>
        </div>
      </td>
      <td>${u.role}</td>
      <td>${u.dept}</td>
      <td>${statusBadge(u.status)}</td>
      <td><span style="font-size:12px;color:var(--text-mid)">${u.profile}</span></td>
      <td><span class="last-access">${u.last_access}</span></td>
      <td>
        <div class="action-btns">
          <button class="act-btn edit"  onclick="openEditModal(${ri})"  title="${d.tip_edit}"><i class="fa-solid fa-pen"></i></button>
          <button class="act-btn reset" onclick="openResetModal(${ri})" title="${d.tip_reset}"><i class="fa-solid fa-key"></i></button>
          <button class="act-btn del"   onclick="confirmDelete(${ri})"  title="${d.tip_del}"><i class="fa-solid fa-trash"></i></button>
        </div>
      </td>
    </tr>`;
  }).join('');
  const info = `${d.showing} <strong>${Math.min(start+1,tot)}-${Math.min(start+PER_PAGE,tot)}</strong> ${d.of} <strong>${tot}</strong> ${d.records}`;
  document.getElementById('pag-info').innerHTML = info;
  if(document.getElementById('tbl-count-info')) document.getElementById('tbl-count-info').innerHTML = `${tot} ${d.records}`;
  renderPagination(totalPages);
}
function renderPagination(total){
  const btns = document.getElementById('pag-btns');
  let html = `<button class="pag-btn" onclick="goPage(${currentPage-1})" ${currentPage<=1?'disabled':''}><i class="fa-solid fa-chevron-left"></i></button>`;
  for(let i=1;i<=total;i++){
    if(i===1||i===total||Math.abs(i-currentPage)<=1){
      html += `<button class="pag-btn ${i===currentPage?'active':''}" onclick="goPage(${i})">${i}</button>`;
    } else if(Math.abs(i-currentPage)===2){
      html += `<button class="pag-btn" disabled>…</button>`;
    }
  }
  html += `<button class="pag-btn" onclick="goPage(${currentPage+1})" ${currentPage>=total?'disabled':''}><i class="fa-solid fa-chevron-right"></i></button>`;
  btns.innerHTML = html;
}
function goPage(p){ const f=getFiltered();const tot=Math.max(1,Math.ceil(f.length/PER_PAGE));if(p<1||p>tot)return;currentPage=p;renderTable(); }

/* ══ MODAIS ══ */
function openAddModal(){
  editIdx = -1;
  const d = TR[currentLang] || TR['pt-BR'];
  document.getElementById('modal-title').textContent = d.modal_add;
  document.getElementById('modal-sub').textContent   = d.modal_sub;
  document.getElementById('f-name').value   = '';
  document.getElementById('f-email').value  = '';
  document.getElementById('f-role').value   = '';
  document.getElementById('f-dept').value   = 'Operações';
  document.getElementById('f-status').value = 'ativo';
  document.getElementById('f-profile').value= 'Analista';
  updateAvatarPreview();
  document.getElementById('modal-user').classList.add('open');
}
function openEditModal(idx){
  editIdx = idx;
  const u = users[idx];
  const d = TR[currentLang] || TR['pt-BR'];
  document.getElementById('modal-title').textContent = d.modal_edit;
  document.getElementById('modal-sub').textContent   = d.modal_sub;
  document.getElementById('f-name').value    = u.name;
  document.getElementById('f-email').value   = u.email;
  document.getElementById('f-role').value    = u.role;
  document.getElementById('f-dept').value    = u.dept;
  document.getElementById('f-status').value  = u.status;
  document.getElementById('f-profile').value = u.profile;
  updateAvatarPreview();
  document.getElementById('modal-user').classList.add('open');
}
function closeModal(id){ document.getElementById(id).classList.remove('open'); }
document.querySelectorAll('.modal-bg').forEach(bg => {
  bg.addEventListener('click', e => { if(e.target === bg) bg.classList.remove('open'); });
});

function updateAvatarPreview(){
  const name = document.getElementById('f-name').value.trim() || 'JD';
  const av   = document.getElementById('av-preview');
  const np   = document.getElementById('av-name-preview');
  av.textContent   = getInitials(name);
  av.style.background = getColor(name);
  if(np) np.textContent = name || '—';
}

function saveUser(){
  const name = document.getElementById('f-name').value.trim();
  if(!name){ document.getElementById('f-name').classList.add('err'); return; }
  document.getElementById('f-name').classList.remove('err');
  const u = {
    id: editIdx>=0 ? users[editIdx].id : `U-${String(users.length+1).padStart(3,'0')}`,
    name,
    email:  document.getElementById('f-email').value.trim()  || `${name.split(' ')[0].toLowerCase()}@bat.com`,
    role:   document.getElementById('f-role').value.trim()   || '—',
    dept:   document.getElementById('f-dept').value,
    status: document.getElementById('f-status').value,
    profile:document.getElementById('f-profile').value,
    last_access: editIdx>=0 ? users[editIdx].last_access : 'Agora',
    online: editIdx>=0 ? users[editIdx].online : false,
  };
  if(editIdx>=0) users[editIdx] = u; else users.push(u);
  closeModal('modal-user');
  updateStats(); renderView();
  showToast((TR[currentLang]||TR['pt-BR']).toast_saved, 'suc');
}

/* RESET PASSWORD */
function openResetModal(idx){
  const chars='ABCDEFGHJKLMNPQRSTUVWXYZabcdefghijkmnpqrstuvwxyz23456789!@#$';
  let pass='BAT@';
  for(let i=0;i<6;i++) pass+=chars[Math.floor(Math.random()*chars.length)];
  document.getElementById('new-pass').textContent = pass;
  document.getElementById('modal-reset').classList.add('open');
}
function copyPass(){
  const pass = document.getElementById('new-pass').textContent;
  navigator.clipboard.writeText(pass).then(()=>showToast((TR[currentLang]||TR['pt-BR']).toast_reset,'info')).catch(()=>{});
}

/* DELETE */
function confirmDelete(idx){
  deleteIdx = idx;
  document.getElementById('modal-confirm').classList.add('open');
  document.getElementById('btn-do-del').onclick = () => {
    users.splice(deleteIdx,1);
    closeModal('modal-confirm');
    updateStats(); renderView();
    showToast((TR[currentLang]||TR['pt-BR']).toast_deleted,'err');
  };
}

/* ══ TOAST ══ */
function showToast(msg, type='suc'){
  const t   = document.getElementById('toast');
  const ico = document.getElementById('toast-icon');
  const icMap = {suc:'fa-check-circle',err:'fa-times-circle',warn:'fa-exclamation-circle',info:'fa-info-circle'};
  document.getElementById('toast-msg').textContent = msg;
  t.className = `toast show ${type}`;
  ico.className = `fa-solid ${icMap[type]||'fa-check-circle'}`;
  clearTimeout(t._t);
  t._t = setTimeout(()=>t.classList.remove('show'),3200);
}

