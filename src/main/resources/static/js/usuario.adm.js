/* ══ AVATAR ══ */
function getInitials(name){
  const p = name.trim().split(' ');
  if(p.length >= 2) return (p[0][0] + p[p.length-1][0]).toUpperCase();
  return (p[0][0] || '?').toUpperCase();
}
function getColor(name){
  let h = 0;
  for(let c of name) h = (h << 5) - h + c.charCodeAt(0);
  return AVATAR_COLORS[Math.abs(h) % AVATAR_COLORS.length];
}
function avatarHTML(name, size=52, extra=''){
  const ini = getInitials(name);
  const col = getColor(name);
  const fs  = Math.round(size * 0.35);
  return `<div class="user-avatar" style="width:${size}px;height:${size}px;font-size:${fs}px;background:${col};${extra}">${ini}</div>`;
}

