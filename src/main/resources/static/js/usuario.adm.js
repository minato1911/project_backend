function updateThemeUI(t){
  const i = document.getElementById('theme-icon');
  const l = document.getElementById('theme-label');
  if(i) i.className = t === 'dark' ? 'fa-solid fa-sun' : 'fa-solid fa-moon';
  if(l) l.textContent = t === 'dark'
    ? (TR[currentLang].theme_light || 'Claro')
    : (TR[currentLang].theme_dark  || 'Escuro');
}

/* ══ LANGUAGE ══ */
function initLang(){
  const s = localStorage.getItem('bat-lang') || 'pt-BR';
  currentLang = s;
  document.getElementById('lang-label').textContent = LANG_CODES[s] || 'PT';
  applyTranslations();
}
function setLang(code){
  currentLang = code;
  localStorage.setItem('bat-lang', code);
  document.getElementById('lang-label').textContent = LANG_CODES[code] || code;
  document.querySelectorAll('.lang-opt').forEach((el, i) => {
    const codes = ['pt-BR','en','es','de','ru','zh'];
    el.classList.toggle('active', codes[i] === code);
  });
  closeLangMenu();
  applyTranslations();
  renderView();
  updateThemeUI(document.documentElement.getAttribute('data-theme'));
}
function applyTranslations(){
  const d = TR[currentLang] || TR['pt-BR'];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.getAttribute('data-i18n');
    if(d[k] !== undefined) el.textContent = d[k];
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const k = el.getAttribute('data-i18n-ph');
    if(d[k]) el.placeholder = d[k];
  });
}
