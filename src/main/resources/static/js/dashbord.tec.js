function renderSectorBars(){
  var active=allTickets.filter(function(t){ return t.status!=='resolvido'; });
  var sectors={};
  active.forEach(function(t){ sectors[t.sector]=(sectors[t.sector]||0)+1; });
  var entries=Object.keys(sectors).map(function(k){return [k,sectors[k]];}).sort(function(a,b){return b[1]-a[1];}).slice(0,6);
  var max=entries.length?entries[0][1]:1;
  var colors=['#005691','#2D9E6B','#E8A020','#6B46C1','#C8A96B','#E53E3E'];
  var el=document.getElementById('sector-bars');
  if(!entries.length){ el.innerHTML='<p style="font-size:12px;color:var(--mu);text-align:center;padding:20px">—</p>'; return; }
  el.innerHTML=entries.map(function(e,i){
    var pct=Math.round((e[1]/max)*100);
    return '<div class="bar-row"><div class="bar-label" title="'+e[0]+'">'+e[0]+'</div><div class="bar-track"><div class="bar-fill" style="width:'+pct+'%;background:'+colors[i%colors.length]+'"><span class="bar-val">'+e[1]+'</span></div></div></div>';
  }).join('');
}

