function timeAgo(ds){
  var diff=Math.floor((Date.now()-parseDate(ds).getTime())/60000);
  if(diff<1) return T('now');
  if(diff<60) return diff+' '+T('min_ago');
  if(diff<1440) return Math.floor(diff/60)+' '+T('hour_ago');
  return parseDate(ds).toLocaleDateString();
}

