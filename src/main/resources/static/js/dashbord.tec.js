/* ============ DONUT: PRIORITY ============ */
function renderDonut(){
  var active=allTickets.filter(function(t){ return t.status!=='resolvido'; });
  var counts={alta:0,media:0,baixa:0};
  active.forEach(function(t){ if(counts[t.priority]!==undefined) counts[t.priority]++; });
  var total=counts.alta+counts.media+counts.baixa;
  document.getElementById('donut-total').textContent=total;
  var colors={alta:'#E53E3E',media:'#E8A020',baixa:'#005691'};
  var svg=document.getElementById('donut-svg');
  var r=46,cx=60,cy=60,circ=2*Math.PI*r;
  if(total===0){ svg.innerHTML='<circle cx="60" cy="60" r="46" fill="none" stroke="var(--br)" stroke-width="14"/>'; document.getElementById('donut-legend').innerHTML=''; return; }
  var offset=0, paths='';
  ['alta','media','baixa'].forEach(function(p){
    if(counts[p]===0) return;
    var frac=counts[p]/total, len=frac*circ;
    paths+='<circle cx="'+cx+'" cy="'+cy+'" r="'+r+'" fill="none" stroke="'+colors[p]+'" stroke-width="14" stroke-dasharray="'+len+' '+(circ-len)+'" stroke-dashoffset="'+(-offset)+'"/>';
    offset+=len;
  });
  svg.innerHTML=paths;
  document.getElementById('donut-legend').innerHTML=['alta','media','baixa'].map(function(p){
    return '<div class="dl-item"><span class="dl-dot" style="background:'+colors[p]+'"></span><span class="dl-name">'+T('prio_'+p)+'</span><span class="dl-val">'+counts[p]+'</span></div>';
  }).join('');
}

