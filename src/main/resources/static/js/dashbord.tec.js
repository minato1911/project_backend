function renderGauge(){
  var done=allTickets.filter(function(t){ return t.status==='resolvido' && t.serviceTime; });
  var secs=done.map(function(t){ return toSec(t.serviceTime); });
  var avg=secs.length?Math.round(secs.reduce(function(a,b){return a+b;},0)/secs.length):0;
  var fast=secs.length?Math.min.apply(null,secs):0;
  var slow=secs.length?Math.max.apply(null,secs):0;
  document.getElementById('gauge-val').textContent=fmtDur(avg);
  document.getElementById('gauge-fast').textContent=secs.length?fmtDur(fast):'—';
  document.getElementById('gauge-slow').textContent=secs.length?fmtDur(slow):'—';
  // gauge ring: avg relative to a 3h max scale
  var maxScale=3*3600;
  var frac=Math.min(avg/maxScale,1);
  var r=54,cx=65,cy=65,circ=2*Math.PI*r;
  var len=frac*circ;
  var color=avg<=3600?'#2D9E6B':avg<=7200?'#E8A020':'#E53E3E';
  document.getElementById('gauge-svg').innerHTML=
    '<circle cx="65" cy="65" r="54" fill="none" stroke="var(--br)" stroke-width="12"/>'+
    '<circle cx="65" cy="65" r="54" fill="none" stroke="'+color+'" stroke-width="12" stroke-linecap="round" stroke-dasharray="'+len+' '+(circ-len)+'"/>';
}

/* ============ TIMELINES ============ */
function prioLabel(p){ return T('prio_'+p); }
function renderTimelines(){
  // received = all sorted by date desc, top 6
  var received=allTickets.slice().sort(function(a,b){return parseDate(b.date)-parseDate(a.date);}).slice(0,6);
  var rEl=document.getElementById('tl-received');
  if(!received.length){ rEl.innerHTML='<div class="tl-empty"><i class="fa-solid fa-inbox"></i><p>'+T('tl_empty')+'</p></div>'; }
  else {
    rEl.innerHTML=received.map(function(t){
      var ico=t.priority==='alta'?'fa-triangle-exclamation':t.priority==='media'?'fa-circle-dot':'fa-circle';
      return '<div class="tl-item"><div class="tl-dot '+t.priority+'"><i class="fa-solid '+ico+'"></i></div><div class="tl-ct"><div class="tl-ct-top"><strong>'+t.id+'</strong><span class="tl-prio '+t.priority+'">'+prioLabel(t.priority)+'</span></div><p>'+t.equip+' — '+t.problem+'</p><div class="tl-meta"><span><i class="fa-solid fa-building"></i> '+t.sector+'</span><span><i class="fa-regular fa-clock"></i> '+timeAgo(t.date)+'</span></div></div></div>';
    }).join('');
  }
  // finished = status 4 sorted by dateEnd desc top 6
  var finished=allTickets.filter(function(t){return t.status==='resolvido';}).sort(function(a,b){return parseDate(b.dateEnd)-parseDate(a.dateEnd);}).slice(0,6);
  var fEl=document.getElementById('tl-finished');
  if(!finished.length){ fEl.innerHTML='<div class="tl-empty"><i class="fa-solid fa-circle-check"></i><p>'+T('tl_empty')+'</p></div>'; }
  else {
    fEl.innerHTML=finished.map(function(t){
      var total=t.serviceTime?fmtDur(toSec(t.travelTime)+toSec(t.serviceTime)):'—';
      return '<div class="tl-item"><div class="tl-dot ok"><i class="fa-solid fa-check"></i></div><div class="tl-ct"><div class="tl-ct-top"><strong>'+t.id+'</strong><span class="tl-prio '+t.priority+'">'+prioLabel(t.priority)+'</span></div><p>'+t.equip+' — '+t.problem+'</p><div class="tl-meta"><span><i class="fa-solid fa-building"></i> '+t.sector+'</span><span><i class="fa-solid fa-stopwatch"></i> '+total+'</span><span><i class="fa-solid fa-user-gear"></i> '+(t.tech||'—')+'</span></div></div></div>';
    }).join('');
  }
}

/* ============ ALERTS ============ */
function renderAlerts(){
  var avail=getAvailable();
  var urgent=getUrgent().filter(function(t){return t.status==='aberto';});
  var exceeded=allTickets.filter(function(t){ return t.status==='andamento' && t.serviceTime && toSec(t.serviceTime)>2*3600; });
  var alerts=[];
  if(urgent.length && dismissedAlerts.indexOf('urgent')<0) alerts.push({type:'urgent',ico:'fa-triangle-exclamation',t:T('alert_urgent_t'),d:T('alert_urgent_d'),act:T('alert_attend')});
  if(avail.length && dismissedAlerts.indexOf('new')<0) alerts.push({type:'new',ico:'fa-bell',t:T('alert_new_t'),d:avail.length+' — '+T('alert_new_d'),act:T('alert_view')});
  if(exceeded.length && dismissedAlerts.indexOf('exceeded')<0) alerts.push({type:'exceeded',ico:'fa-clock',t:T('alert_exceeded_t'),d:T('alert_exceeded_d'),act:T('alert_view')});
  var el=document.getElementById('alerts');
  el.innerHTML=alerts.map(function(a){
    return '<div class="alert '+a.type+'"><div class="alert-ico"><i class="fa-solid '+a.ico+'"></i></div><div class="alert-ct"><strong>'+a.t+'</strong><span>'+a.d+'</span></div><button class="alert-act" onclick="showToast(T(\'toast_nav\'),\'info\')">'+a.act+'</button><button class="alert-close" onclick="dismissAlert(\''+a.type+'\')" aria-label="Fechar"><i class="fa-solid fa-xmark"></i></button></div>';
  }).join('');
}
function dismissAlert(type){ dismissedAlerts.push(type); renderAlerts(); showToast(T('toast_alert_dismiss'),'info'); }

