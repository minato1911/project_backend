var sessions=[];
function buildSessions(){
  var now=new Date();
  function f(d){ var dd=String(d.getDate()).padStart(2,'0'),mm=String(d.getMonth()+1).padStart(2,'0'),hh=String(d.getHours()).padStart(2,'0'),mi=String(d.getMinutes()).padStart(2,'0'); return dd+'/'+mm+'/'+d.getFullYear()+' '+hh+':'+mi; }
  function ago(m){ return f(new Date(now-m*60000)); }
  sessions=[
    {id:'s1',device:'win',ip:'192.168.0.45',time:ago(2),current:true},
    {id:'s2',device:'android',ip:'10.0.2.18',time:ago(1200),current:false},
    {id:'s3',device:'ios',ip:'10.0.2.33',time:ago(4320),current:false}
  ];
}
function renderSessions(){
  var devIcons={win:'fa-laptop',android:'fa-mobile-screen',mac:'fa-laptop',ios:'fa-mobile-screen-button'};
  var devKeys={win:'dev_win',android:'dev_android',mac:'dev_mac',ios:'dev_ios'};
  document.getElementById('session-list').innerHTML=sessions.map(function(s){
    var icoCls=s.current?'session-ico current':'session-ico';
    var right=s.current?'<span class="session-badge">'+T('acc_current')+'</span>':'<button class="session-revoke" onclick="revokeSession(\''+s.id+'\')">'+T('revoke')+'</button>';
    return '<div class="session-item"><div class="'+icoCls+'"><i class="fa-solid '+(devIcons[s.device]||'fa-laptop')+'"></i></div><div class="session-info"><strong>'+T(devKeys[s.device])+'</strong><span>'+s.ip+' · '+s.time+'</span></div>'+right+'</div>';
  }).join('');
}
function revokeSession(id){
  sessions=sessions.filter(function(s){return s.id!==id;});
  renderSessions();
  showToast(T('toast_revoked'),'suc');
}

