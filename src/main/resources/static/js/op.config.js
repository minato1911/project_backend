/* ============================================================ INIT */
function init(){
  // language
  var lang=localStorage.getItem('bat-cfg-lang')||'pt'; CL=lang;
  document.getElementById('lang-lbl').textContent=lang.toUpperCase();
  document.querySelectorAll('.lo').forEach(function(el,i){ el.classList.toggle('active',['pt','en','es','de','ru'][i]===lang); });
  // theme
  initTheme();
  // accessibility states
  var hc=localStorage.getItem('bat-cfg-hc')||localStorage.getItem('bat-op-hc')||'0';
  if(hc==='1'){ document.documentElement.setAttribute('data-hc','1'); document.getElementById('sw-hc').checked=true; }
  var libras=localStorage.getItem('bat-cfg-libras')||'1';
  if(libras==='0'){ document.getElementById('sw-libras').checked=false; document.querySelector('.libras-wrap').style.display='none'; }
  var font=localStorage.getItem('bat-op-fs')||localStorage.getItem('bat-cfg-font')||'1';
  document.documentElement.style.fontSize=(font*100)+'%';
  document.getElementById('font-range').value=font;
  document.getElementById('font-val').textContent=Math.round(font*100)+'%';
  var rm=localStorage.getItem('bat-op-rm')||localStorage.getItem('bat-cfg-rm')||'0';
  if(rm==='1'){ document.getElementById('sw-rm').checked=true; toggleRM(document.getElementById('sw-rm')); }
  var reader=localStorage.getItem('bat-cfg-reader')||'0';
  if(reader==='1') document.getElementById('sw-reader').checked=true;
  // notifications
  var ne=localStorage.getItem('bat-cfg-notif-email'); if(ne!==null) document.getElementById('sw-email').checked=(ne==='1');
  var ns=localStorage.getItem('bat-cfg-notif-sys'); if(ns!==null) document.getElementById('sw-sys').checked=(ns==='1');
  var nso=localStorage.getItem('bat-cfg-notif-sound'); if(nso!==null) document.getElementById('sw-sound').checked=(nso==='1');
  // render
  applyTR();
  initUser();
  syncThemeCards();
  syncLangCards();
  buildSessions(); renderSessions();
  buildAccess(); renderAccess();
}
document.addEventListener('DOMContentLoaded',init);
