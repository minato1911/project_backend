const LANG_CODES = { 'pt-BR': 'PT', 'en': 'EN', 'es': 'ES', 'de': 'DE', 'ru': 'RU', 'zh': '中文' };
let currentLang = 'pt-BR';

/* ── THEME ── */
function initTheme() {
    const s = localStorage.getItem('bat-theme') || 'light';
    document.documentElement.setAttribute('data-theme', s);
    updateThemeUI(s);
}

function toggleTheme() {
    const cur = document.documentElement.getAttribute('data-theme');
    const next = cur === 'dark' ? 'light' : 'dark';
    document.documentElement.setAttribute('data-theme', next);
    localStorage.setItem('bat-theme', next);
    updateThemeUI(next);
}

function updateThemeUI(t) {
    const icon = document.getElementById('theme-icon');
    const label = document.getElementById('theme-label');
    if (icon) icon.className = t === 'dark' ? 'fa-solid fa-sun' : 'fa-solid fa-moon';
    if (label) label.textContent = t === 'dark' ? TR[currentLang].theme_light : TR[currentLang].theme_dark;
}

/* ── LANGUAGE ── */
function initLang() {
    const s = localStorage.getItem('bat-lang') || 'pt-BR';
    currentLang = s;
    applyTranslations();
    document.getElementById('lang-label').textContent = LANG_CODES[s] || 'PT';
    document.querySelectorAll('.lang-opt').forEach(el => {
        el.classList.toggle('active', el.getAttribute('onclick').includes(s));
    });
}

function setLang(code) {
    currentLang = code;
    localStorage.setItem('bat-lang', code);
    applyTranslations();
    document.getElementById('lang-label').textContent = LANG_CODES[code] || code;
    document.querySelectorAll('.lang-opt').forEach(el => {
        el.classList.toggle('active', el.getAttribute('onclick').includes("'" + code + "'"));
    });
    closeLangMenu();
    updateThemeUI(document.documentElement.getAttribute('data-theme'));
}

function applyTranslations() {
    const d = TR[currentLang] || TR['pt-BR'];
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const k = el.getAttribute('data-i18n');
        if (d[k]) el.textContent = d[k];
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
        const k = el.getAttribute('data-i18n-ph');
        if (d[k]) el.placeholder = d[k];
    });
}

function toggleLangMenu() { document.getElementById('lang-menu').classList.toggle('open'); }

function closeLangMenu() { document.getElementById('lang-menu').classList.remove('open'); }
document.addEventListener('click', e => { if (!e.target.closest('.lang-wrap')) closeLangMenu(); });

/* ── LIBRAS ── */
function toggleLibras() { document.getElementById('libras-panel').classList.toggle('open'); }

/* Mostrar/Ocultar Senha */
function togglePwd() {
    const input = document.getElementById('senha');
    const icon = document.getElementById('eye-icon');

    input.type = input.type === 'password' ? 'text' : 'password';
    icon.className = input.type === 'password' ?
        'fa-solid fa-eye' :
        'fa-solid fa-eye-slash';
}

/* Login */
function fazerLogin() {
    const usuario = document.getElementById('usuario').value;
    const senha = document.getElementById('senha').value;

    // Administrador
    if (usuario === 'admin@bat' && senha === '123456') {
        window.location.href = '/adm/dashboard';
    }
    // Operador
    else if (usuario === 'operador@bat' && senha === '123456') {
        window.location.href = '/operador/dashboard';
    }
    // Login inválido
    else {
        alert('Usuário ou senha inválidos!');
    }
}

async function doLogin() {
    const em = document.getElementById('email').value.trim();
    const pw = document.getElementById('senha').value;
    const btn = document.getElementById('btnLogin');
    if (!em || !pw) {
        btn.style.animation = 'none';
        btn.offsetHeight;
        btn.style.animation = 'shake .35s ease';
        setTimeout(() => btn.style.animation = '', 400);
        return;
    }

    btn.classList.add('loading');
    btn.disabled = true;

    try {
        const response = await fetch('/api/auth/login', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ email: em, senha: pw })
        });

        if (!response.ok) {
            const message = await response.text();
            throw new Error(message || 'Email ou senha invalidos.');
        }

        const user = await response.json();
        localStorage.setItem('fixon-user', JSON.stringify(user));
        localStorage.setItem('bat-user-id', user.id);
        localStorage.setItem('bat-user-role', user.perfil);

        if (user.perfil === 'OPERADOR') {
            localStorage.setItem('bat-op-name', user.nome);
            localStorage.setItem('bat-op-email', user.email);
        } else if (user.perfil === 'TECNICO') {
            localStorage.setItem('bat-tech-name', user.nome);
            localStorage.setItem('bat-tech-email', user.email);
        }

        window.location.href = user.redirectUrl || '/';
    } catch (error) {
        alert(error.message);
        btn.style.animation = 'none';
        btn.offsetHeight;
        btn.style.animation = 'shake .35s ease';
        setTimeout(() => btn.style.animation = '', 400);
    } finally {
        btn.classList.remove('loading');
        btn.disabled = false;
    }
}

