function togglePass(inputId,iconId){
  const inp=document.getElementById(inputId);
  const ico=document.getElementById(iconId);
  inp.type = inp.type==='password'?'text':'password';
  ico.className = inp.type==='password'?'fa-solid fa-eye':'fa-solid fa-eye-slash';
}
function checkStrength(){
  const v=document.getElementById('f-pass-new').value;
  const bar=document.getElementById('pass-strength');
  const d=TR[currentLang]||TR['pt-BR'];
  if(!v){ bar.style.width='0'; return; }
  let score=0;
  if(v.length>=8)  score++;
  if(/[A-Z]/.test(v)) score++;
  if(/[0-9]/.test(v)) score++;
  if(/[^A-Za-z0-9]/.test(v)) score++;
  const map=[{w:'25%',bg:d.pass_weak,c:'#E53E3E'},{w:'50%',bg:d.pass_medium,c:'#E8A020'},{w:'75%',bg:d.pass_medium,c:'#E8A020'},{w:'100%',bg:d.pass_strong,c:'#2D9E6B'}];
  const s=map[Math.max(0,score-1)];
  bar.style.width=s.w; bar.style.background=s.c; bar.title=s.bg;
}

