/* ══ AVATAR utils ══ */
const AV_COLORS = ['#005691','#002B5B','#2D9E6B','#6B46C1','#E8A020','#0072BC','#C0392B'];
function initials(n){ const p=n.trim().split(' '); return p.length>=2?(p[0][0]+p[p.length-1][0]).toUpperCase():(p[0][0]||'?').toUpperCase(); }
function avColor(n){ let h=0; for(const c of n) h=(h<<5)-h+c.charCodeAt(0); return AV_COLORS[Math.abs(h)%AV_COLORS.length]; }
function refreshAvatars(){
  const nm = document.getElementById('f-name').value.trim()||'Ana Carolina Lima';
  const ini = initials(nm); const col = avColor(nm);
  const ma = document.getElementById('main-avatar');
  const txt = document.getElementById('main-av-txt');
  const au = document.getElementById('av-up-circle');
  if(txt) txt.textContent = ini;
  if(ma)  ma.style.background = col;
  if(au){ au.textContent = ini; au.style.background = col; }
}

