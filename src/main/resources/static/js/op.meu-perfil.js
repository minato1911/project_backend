/* EMAIL VALIDATION */
function validateEmail(){
  var el=document.getElementById('f-email');
  var v=el.value.trim();
  var ok=/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(v);
  el.classList.toggle('err-field',!ok&&v.length>0);
  el.classList.toggle('ok-field',ok);
  document.getElementById('e-email').classList.toggle('show',!ok&&v.length>0);
  return ok;
}

/* PHONE MASK */
function maskPhone(el){
  var v=el.value.replace(/\D/g,'').slice(0,11);
  var out='';
  if(v.length>0) out='('+v.slice(0,2);
  if(v.length>=2) out+=') ';
  if(v.length>=2) out+=v.slice(2,7);
  if(v.length>=7) out+='-'+v.slice(7,11);
  el.value=out;
}

