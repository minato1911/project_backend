/* PASSWORD STRENGTH */
function checkStrength(){
  var v=document.getElementById('f-newpw').value;
  var wrap=document.getElementById('pw-strength');
  if(!v){ wrap.style.display='none'; return; }
  wrap.style.display='block';
  var score=0;
  if(v.length>=8) score++;
  if(/[a-z]/.test(v)&&/[A-Z]/.test(v)) score++;
  if(/\d/.test(v)) score++;
  if(/[^a-zA-Z0-9]/.test(v)) score++;
  var segs=['seg1','seg2','seg3','seg4'];
  segs.forEach(function(s,i){ var el=document.getElementById(s); el.className='pw-seg'+(i<score?(' s'+score):''); });
  var labels=['','pw_weak','pw_fair','pw_good','pw_strong'];
  document.getElementById('pw-label').textContent=T(labels[score]||'pw_weak');
}

/* PASSWORD FORM CHECK */
